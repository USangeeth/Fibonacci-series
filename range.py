# printing positive numbers in the given range
# list of numbers
list1 = [12,-7,5,64,-14]
num = 0
  
# using while loop     
while(num < len(list1)):
      
    # checking condition
    if list1[num] >= 0:
        print(list1[num], end = " ")
      
    # increment num 
    num += 1
    
